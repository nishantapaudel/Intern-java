public class Mathinjava {
    public static void main(String[] args) {
        System.out.println("Max num: "+Math.max(20,30));
        System.out.println("Min num: "+Math.min(20,30));
        System.out.println("Round value: "+Math.round(20.4)); //20
        System.out.println("Floor value: "+Math.floor(10.99)); //10
        System.out.println("Ceil value: "+Math.ceil(10.01)); //11
        System.out.println("Square Root: "+Math.sqrt(25)); //5
        System.out.println("Cube: "+Math.pow(10,2)); // 100
    }
}
